                                    <h1 class="post-title">
                                        Sosialisasi Arsitektur SPBE, Peta Rencana SPBE dan Manajemen Risiko SPBE
                                    </h1>
                                    <div class="post-meta">
                                        <span class="post-author"><img alt="Admin" class="post-author-avatar"
                                                src="//blogger.googleusercontent.com/img/b/R29vZ2xl/AVvXsEjgibrrbd8cyImsqWJQvFyi_O_Q8bGdBoeG5dCUOGyG5hgA052s4Tz507ri3lmrhj8cZVvhI91qW9I498hFJAkCUPZjKOkcmrw_BVApQYbM9nOBrRmxCmWHbeJykDXrsOU/w20/android-chrome-192x192.png"><a
                                                href="https://www.blogger.com/profile/14418033569033797261"
                                                target="noopener" title="Admin">Admin</a></span>
                                        <span class="post-date published" datetime="2023-11-25T14:16:00+08:00">November
                                            25, 2023</span>
                                    </div>
                                    <div class="post-body post-content">
                                        Kepala Dinas Komunikasi, Informatika, Dan Statistik Kabupaten Malang, Windiasti
                                        Kartika S.T.,M.T membuka Sosialisasi dan Bimtek Manajemen Risiko Sistem
                                        Pemerintahan Berbasis Elektronik (SPBE), di Ballroom Hotel Rattan Inn. Kamis
                                        (23/11).<br><br>Turut Hadir dalam kegiatan Kepala Bidang Aptika Diskominfotik
                                        Kabupaten Malang, H Agung Saptoto, Kepala Bidang Komunikasi Diskominfotik, M
                                        Yamani, Sejumlah pimpinan SKPD beserta jajaran di lingkup pemerintah Kabupaten
                                        Malang. Turut berhadir juga, Analis Kebijakan Pertama Kemenpanrb, Aisyah
                                        Nusa Ramadhana dan Muhammad Elfan Budi Nugroho sebagai narasumber pada kegiatan
                                        Bimtek tersebut.<br><br>Dalam sambutan Kepala Dinas Kominfotik Kabupaten Malang,
                                        Windiasti Kartika S.T.,M.T., menyoroti pencapaian signifikan Pemerintah Kota
                                        Malang dalam menerapkan Sistem Pemerintahan Berbasis Elektronik (SPBE).
                                        Dokumentasi SPBE telah berhasil diimplementasikan dengan sukses sejak tahun
                                        2022, melibatkan berbagai Satuan Kerja Perangkat Daerah (SKPD) dalam mencatat
                                        proses bisnis dan mengidentifikasi program yang dibutuhkan. Dokumen arsitektur
                                        SPBE menjadi panduan utama bagi SKPD, memperkuat efisiensi, transparansi, dan
                                        akuntabilitas dalam penyelenggaraan pemerintahan.<br><br>"Pentingnya manajemen
                                        risiko dalam konteks SPBE ini yaitu Risiko potensial, seperti gangguan sistem
                                        aplikasi merupakan tantangan yang harus diatasi. Hal ini tidak hanya untuk
                                        mencapai efisiensi melalui teknologi informasi, tetapi juga melalui
                                        langkah-langkah proaktif dalam mengidentifikasi dan mengelola risiko yang
                                        mungkin timbul selama implementasi SPBE," Ujar Kepala Diskominfotik.<br><br>"Ini
                                        mencerminkan bukan hanya pencapaian praktis Pemerintah Kabupaten Malang dalam
                                        menerapkan SPBE, tetapi juga kesadaran kita semua terhadap perlunya manajemen
                                        risiko yang cermat dalam menghadapi dinamika penerapan teknologi informasi dalam
                                        penyelenggaraan pemerintahan daerah," tambahnya.<div><br></div>
                                        <div>
                                            <div>Sementara itu, Kepala Bidang Aptika, H Agung Saptoto menyampaikan bahwa
                                                manajemen risiko SPBE dianggap krusial dalam mencapai tata kelola
                                                pemerintahan yang efektif, transparan, akuntabel, dan pelayanan publik
                                                berkualitas.</div>
                                            <div><br></div>
                                            <div>"Harapannya, acara ini memberikan manfaat besar bagi peserta,
                                                memungkinkan mereka menerapkan ilmu di SKPD masing-masing dan berbagi
                                                pengetahuan dengan sesama ASN," pungkasnya.<br><br>Dalam kegiatan ini
                                                juga dilaksanakan sosialisasi Arsitektur SPBE dan Peta Rencana SPBE
                                                Pemerintah Kabupaten Malang.</div>
