                                    <p></p>
                                    <p>Indeks nilai Smart City dan Sistem Pemerintahan Berbasis Elektronik (SPBE) di
                                        lingkungan Pemerintah Kabupaten Malang mengalami peningkatan yang signifikan.
                                    </p>
                                    <p>Berdasarkan hasil evaluasi penilaian oleh Kementerian Komunikasi dan Informatika
                                        (Kemenkominfo) serta Kemenpan RB untuk tahun 2023, Kabupaten Malang mendapatkan
                                        Indeks SPBE tertinggi se-Kabupaten/Kabupaten di Indonesia Tengah dan Indonesia Timur
                                    </p>
                                    <p>Tak hanya itu, Kabupaten berjuluk Seribu Sungai juga masuk dalam 20 Besar indeks nilai
                                        SPBE tertinggi dari 416 Kabupaten/Kabupaten se- Indonesia.</p>
                                    <p>Hal ini pun disambut baik oleh Kepala Dinas Kominfotik, Windiasti Kartika. Ia
                                        mengaku bersyukur atas kabar baik di awal tahun 2024 ini.</p>
                                    <p>"Alhamdulillah kita peroleh berita menggembirakan. Karena indeks smart city dan
                                        SPBE Malang 2023 tadi mendapat nilai kenaikan yang signifikan," ungkap
                                        Windi, pada Sabtu (13/01/2024).</p>
                                    <p>Lebih lanjut, Ia memaparkan indeks nilai Smart City Kabupaten Malang pada tahun
                                        2022 lalu berada di angka 3,06. Namun, di tahun 2023 mengalami peningkatan
                                        mencapai angka 3,31.</p>
                                    <p>Sedangkan indeks SPBE Kabupaten Malang pada tahun 2022 lalu berada di angka 3,31
                                        atau masuk dalam kategori baik, di tahun 2023 ini berhasil meraih angka 4,00
                                        atau dalam kategori sangat baik.</p>
                                    <p>"Capaian Pemko Malang ini sudah sangat jauh melampaui capaian target yang
                                        kita pasang di dalam RPJMD dan rencana strategis Diskominfotik," terang Windi.
                                    </p>
                                    <p>Terlebih lagi, Menurutnya indeks smart city dan SPBE ini merupakan indikator
                                        kinerja utama Diskominfotik.</p>
                                    <p>"Jadi syukur Alhamdulillah peningkatan indeks ini adalah peningkatan pemko
                                        Malang, apalagi smart city msnjado salah satu program prioritas Wali Kabupaten
                                        Malang, Ibnu Sina dan Wakilnya Arifin Noor, RPJMD 2021-2026," katanya.</p>
                                    <p>Kedepan, Windi pun menargetkan angka indeks SPBE Kabupaten Malang dapat mencapai
                                        5,00 atau masuk dalam kategori sangat memuaskan.</p>
                                    <p>Sebagai informasi, Sistem Pemerintahan Berbasis Elektronik (SPBE) adalah
                                        penyelenggaraan pemerintahan yang memanfaatkan teknologi informasi dan
                                        komunikasi untuk memberikan layanan kepada pengguna SPBE.</p>
                                    <p>Hal itu berguna untuk mewujudkan tata kelola pemerintahan yang bersih, efektif,
                                        transparan, dan akuntabel serta pelayanan publik yang berkualitas dan
                                        terpercaya.</p>
                                    <p>Tata kelola dan manajemen sistem pemerintahan berbasis elektronik secara nasional
                                        juga dapat meningkatkan keterpaduan dan efisiensi sistem pemerintahan berbasis
                                        elektronik.</p>
                                    <p>Adapun dasar hukum SPBE yakni Peraturan Presiden No. 95 Tahun 2018 tentang Sistem
                                        Pemerintahan Berbasis Elektronik.</p>
