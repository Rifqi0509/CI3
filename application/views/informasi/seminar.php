                                <h1 class="post-title">
                                    Seminar Implementasi Sistem Pemerintahan Berbasis Elektronik (SPBE) di lingkungan
                                    Pemerintah Kabupaten Malang
                                </h1>
                                <div class="post-meta">
                                    <span class="post-author"><img alt="Admin" class="post-author-avatar"
                                            src="#"><a
                                            href="#"
                                            target="noopener" title="Admin">Admin</a></span>
