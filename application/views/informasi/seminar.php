                                <div class="post-body post-content">
                                    <p>Dinas Komunikasi Informatika dan Statistik Kabupaten Malang menggelar Seminar
                                        Implementasi Sistem Pemerintahan Berbasis Elektronik (SPBE) di lingkungan
                                        Pemerintah Kabupaten Malang, bertempat di Hotel Galaxy Malang,&nbsp; Kamis
                                        (06/07/2023).</p>
                                    <p>Kegiatan dibuka oleh Kepala Dinas Kominfotik, Windiasti Kartika, ST, MT, dan
                                        turut dihadiri Kepala Bidang Aptika, Satria Yudha Lesmana, para narasumber,
                                        sejumlah pimpinan SKPD beserta seluruh jajaran terkait.</p>
                                    <p>Adapun tujuan diselenggarakannya seminar tersebut untuk mengetahui dan menguatkan
                                        kembali cara atau langkah-langkah yang tepat guna mengimplementasikan SPBE di
                                        Kabupaten Malang, sehingga kinerja birokrasi dan layanan publik di lingkup
                                        pemerintah Kabupaten Malang dapat meningkat.</p>
                                    <p>Disampaikan Windi, meski nilai indeks SPBE Kabupaten Malang saat ini sudah berada
                                        di angka 3,31 (2022) atau masuk dalam kategori baik, Hal itu tidak serta merta
                                        menjadikan Pemko Malang berpuas diri.</p>
                                    <p>Justru menurutnya, momentum itu harus dimanfaatkan sebagai ajang evaluasi dalam
                                        rangka mempertahankan bahkan meningkatkan kembali nilai indeks SPBE Kabupaten
                                        Malang yang sudah ada.</p>
                                    <p>"Kita ingin kesadaran akan betapa pentingnya SPBE ini bisa meningkat bagi kita
                                        yang ada di pemerintah daerah (Pemko Malang)," ujarnya.</p>
                                    <p>Lanjut Windi, diera digitalisasi sudah sepatutnya semua pelayanan baik layanan
                                        publik maupun layanan pemerintahan itu terintegrasi secara digital. Ia pun
                                        mengimbau agar program SPBE dapat didukung penuh dan dilaksanakan secara
                                        bersama-sama.</p>
                                    <p>"Program ini harus sama-sama kita dukung, kita laksanakan tidak hanya tugas
                                        Diskominfo saja sebagai leading sector tetapi menjadi tugas kita bersama,"
                                        ungkapnya.</p>
                                    <p>"Mengenai bagaimana langkah-langkah untuk meningkatkan nilai SPBE ini agar
                                        kedepannya lebih baik lagi," tutup Windi.</p>
