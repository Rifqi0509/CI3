                                <div class="post-body post-content">
                                    <p>Pemerintah Kota (Pemko) Malang melalui Dinas Komunikasi Informatika dan
                                        Statistik Kabupaten Malang (Kominfotik) meraih Terbaik 3 pada ajang Kalsel
                                        Innovation Award (KIA) Tahun 2023.</p>
                                    <p>Penghargaan itu diserahkan langsung oleh Asisten Pemerintahan dan Kesra Provinsi
                                        Kalimantan Selatan, Nurul Fajar Desira kepada Kepala Diskominfotik, Windiasti
                                        Kartika, ST, MT bertempat di Gedung K.H. Idham Chalid, Provinsi Kalimantan
                                        Selatan, Kamis 31/08/2023.</p>
                                    <p>Kegiatan pun turut dihadiri Sekretaris Daerah (Sekda) Kabupaten Malang, Ikhsan
                                        Budiman beserta sejumlah pejabat penting lainnya.</p>
